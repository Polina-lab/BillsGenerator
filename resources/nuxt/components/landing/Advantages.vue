  name: "Advantages",
  data () {
    let swiperOptions = {
      autoHeight: true,
      slidesPerView: 1,
      spaceBetween: 30,
      centeredSlides: false,
      loop: true,
      grabCursor: true,
      allowTouchMove: true,
      enabled: true,
    }
    return {
    mobileScreen: false,
    ok: true,
    advantages: [
      { id: 1, name: 'reasons', isDescriptionOpenDesktop: false,  openDescriptionNameDesktop: null, color: 'Green', data: [
