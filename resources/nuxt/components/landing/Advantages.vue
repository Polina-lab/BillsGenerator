        { url: 'helpful_green.svg', text: 'helpful', url_white: 'helpful_white.svg' },
        { url: 'secure_green.svg', text: 'secure' },
      ]},
      { id: 2, name: 'problems', isDescriptionOpenDesktop: false, openDescriptionNameDesktop: null, color: 'Blue', data: [
        { url: 'time_blue.svg', text: 'time' },
        { url: 'maintain_blue.svg', text: 'maintain', url_white: 'maintain_whiteG.svg'},
        { url: 'professional_blue.svg', text: 'professional' },
      ]}
    ],
    descriptions: {
      simple: null,
      helpful: null,
      secure: null,
      time: null,
      maintain: null,
      professional: null,
    },
    swiperOptionsGreen: {
      ...swiperOptions,
      navigation: {
        nextEl: '.fa-arrow-right.green_theme',
        prevEl: '.fa-arrow-left.green_theme'
      },
            pagination: {
        el: '.swiper-pagination',
        clickable: true,
        type: 'bullets'
      }
    },
    swiperOptionsBlue: {
      ...swiperOptions,
      navigation: {
        nextEl: '.fa-arrow-right.blue_theme',
        prevEl: '.fa-arrow-left.blue_theme'
      },
            pagination: {
        el: '.swiper-pagination',
        clickable: true,
        type: 'bullets'
      }
    }
    }
  },
  methods:{
    capitalizeFirstLetter(word) {
      return word[0].toUpperCase() + word.slice(1);
    },

    openSingle(cardName, sectionName) {
      for (let i = 0; i < this.advantages.length; i++) {
        if (this.advantages[i].name === sectionName) {
          for (let j = 0; j < this.advantages[i].data.length; j++) {
            this.descriptions[this.advantages[i].data[j].text] = false;
          }
          this.advantages[i].openDescriptionNameDesktop = cardName;
          this.advantages[i].isDescriptionOpenDesktop = true;
        }
      }
      this.descriptions[cardName] = true;
    }
  }
}
